/// Design tokens for consistent spacing across the app.
/// Use these instead of magic numbers for padding, margins, and gaps.
abstract class AppSpacing {
  AppSpacing._();

  static const double xs = 4;
  static const double sm = 8;
  static const double md = 12;
  static const double lg = 16;
  static const double xl = 24;
  static const double xxl = 32;
}

/// Design tokens for consistent border radii across the app.
/// Use these instead of magic numbers for corners, cards, and inputs.
abstract class AppRadius {
  AppRadius._();

  static const double sm = 8;
  static const double md = 12;
  static const double lg = 16;
  static const double xl = 20;
}
